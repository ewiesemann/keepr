<template>
  <div>
    <div class="login-page">
      <form @submit.prevent="userLogin">
        <input type="text" name="email" id="email" v-model="login.email" placeholder="Email Address">
        <input type="password" name="password" id="password" v-model="login.password" placeholder="Password">
        <button type="submit">LOGIN</button>
      </form>
    </div>

    <div class="register-page">
      <form @submit.prevent="userRegister">
        <input type="text" name="name" id="name" v-model="register.name" placeholder="User Name">
        <input type="text" name="regEmail" id="regEmail" v-model="register.email" placeholder="Email Address">
        <input type="password" name="regPassword" id="regPassword" v-model="register.password" placeholder="Password">
        <button type="submit">Register</button>
      </form>
    </div>
  </div>
</template>

<script>
  import router from '../router'
  export default {
    name: 'login',
    data() {
      return {
        user:{
          email: '',
          password: ''
        }
      }
    },
    mounted() {
      this.$store.dispatch("authenticate")
    },
    computed: {
    },
    methods: {
      login(){
        this.$store.dispatch('login', this.user)
      },
      register(){
        this.$store.dispatch('register', this.user)
      }
    }
  }
</script>

